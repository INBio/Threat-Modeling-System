package org.inbio.modeling.core.model;

import java.util.List;
import java.util.Map;
import org.inbio.modeling.core.dto.CategoryDTO;
import org.inbio.modeling.core.layer.LayerType;

/**
 *
 * @author asanabria
 */
public class GRASSLayer {

	int minValue=-1;
	int maxValue=-1;
	private String name;
	private LayerType type;
	private String description;
	private Map<String,String>	columns = null;
	private List<CategoryDTO>	categories = null;

	public GRASSLayer() { }

	public GRASSLayer(String name) {
		this.name = name;
	}

	public List<CategoryDTO> getCategories() {
		return categories;
	}

	public void setCategories(List<CategoryDTO> categories) {
		this.categories = categories;
	}

	public Map<String, String> getColumns() {
		return columns;
	}

	public void setColumns(Map<String, String> columns) {
		this.columns = columns;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public LayerType getType() {
		return type;
	}

	public void setType(LayerType type) {
		this.type = type;
	}

	public int getMaxValue() {
		return maxValue;
	}

	public void setMaxValue(int maxValue) {
		this.maxValue = maxValue;
	}

	public int getMinValue() {
		return minValue;
	}

	public void setMinValue(int minValue) {
		this.minValue = minValue;
	}
}